package com.bgs.pojo;


public class Shops {

  private Integer id;
  private String shopsCode;
  private String shopsPassword;
  private String shopsName;
  private String phone;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getShopsCode() {
    return shopsCode;
  }

  public void setShopsCode(String shopsCode) {
    this.shopsCode = shopsCode;
  }


  public String getShopsPassword() {
    return shopsPassword;
  }

  public void setShopsPassword(String shopsPassword) {
    this.shopsPassword = shopsPassword;
  }


  public String getShopsName() {
    return shopsName;
  }

  public void setShopsName(String shopsName) {
    this.shopsName = shopsName;
  }


  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }

}
